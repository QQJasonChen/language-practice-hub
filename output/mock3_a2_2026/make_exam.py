def build():
    d = json.loads((HERE / 'data.json').read_text(encoding='utf-8'))
    segs = d['ai_data']['segments']
    info = d.get('video_info', {})
    scenarios = []
    for i, sc in enumerate(MOCK3['scenarios'], 1):
        a, b = sc['dialogue']
        dlg = pull_dialogue(segs, a, b)
        qs = []
        for q in sc.get('questions', []):
            qs.append({
                'n': q['n'], 't': q['t'],
                'reconstructed': q.get('reconstructed', False),
                'q_nl': q['q_nl'], 'q_zh': q['q_zh'],
                'options': [{'nl': o[0], 'zh': o[1]} for o in q['options']],
                'answer': q['answer'],
                'source': [{'t': s[0], 'nl': s[1], 'zh': s[2]} for s in q['source']],
                'explain': q['explain'],
                'trap': q.get('trap', ''),
            })
        scenarios.append({
            'n': i,
            'title_zh': sc['title_zh'], 'title_nl': sc['title_nl'],
            'kind': sc['kind'], 'context_zh': sc['context_zh'],
            'start': dlg[0]['t'] if dlg else '',
            'no_questions': sc.get('no_questions', False),
            'dialogue': dlg,
            'vocab': sc['vocab'],
            'patterns': sc['patterns'],
            'questions': qs,
        })
    exam = {
        'video_id': MOCK3['video_id'],
        'title': info.get('title', ''),
        'channel': info.get('channel', ''),
        'duration': info.get('duration', 0),
        'exam_type': MOCK3['exam_type'],
        'intro': MOCK3['intro'],
        'n_questions': sum(len(s['questions']) for s in scenarios),
        'scenarios': scenarios,
    }
    out = HERE / 'exam.json'
    out.write_text(json.dumps(exam, ensure_ascii=False, indent=2), encoding='utf-8')
    print(f"✓ {out.name} — {len(scenarios)} scenarios, {exam['n_questions']} questions")


if __name__ == '__main__':
    build()
